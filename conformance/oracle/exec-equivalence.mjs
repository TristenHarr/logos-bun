// conformance/oracle/exec-equivalence.mjs — the §6.4 bundler equivalence oracle.
//
// A bundler's OUTPUT BYTES may legally differ between two implementations (different var
// mangling, chunk ordering, whitespace). What must NOT differ is what the bundle DOES. So we
// don't diff bundle text — we RUN the bundle under two runtimes and byte-compare the PROGRAM
// OUTPUT. Sourcemaps get their own comparator: not text-diffed either, but compared by
// DECODED mappings (position -> position), which lands for real at P6.
//
// Scope discipline (CLAUDE.md R9, stub honesty): the exec harness below is IMPLEMENTED and
// real for its narrow job. The sourcemap comparator is an explicit STUB — interface frozen,
// body throws. A stub that silently "passes" would let a whole P6 subsystem look green while
// doing nothing; that is the exact failure this file is built to make impossible. Every
// unimplemented surface FAILS LOUD.
import { spawnSync } from "node:child_process";

/**
 * Run one JS bundle under two runtimes and byte-compare stdout (the program's output).
 * IMPLEMENTED surface: `{ file }` — a path to a self-contained bundle each runtime executes.
 *
 * @param {object} o
 * @param {string}   o.file            path to the bundle to execute under both runtimes
 * @param {string}   o.runtimeA        e.g. the oracle-bun binary
 * @param {string}   o.runtimeB        e.g. `node` (or logos-bun)
 * @param {string[]} [o.argv]          program argv (passed to both)
 * @param {string}   [o.cwd]           working directory for both
 * @param {object}   [o.env]           env for both
 * @param {string}   [o.stdin]         stdin fed to both
 * @returns {{equal:boolean, exitA:number|null, exitB:number|null, stdoutEqual:boolean,
 *            firstDiffLine:number|null, aStdout:string, bStdout:string}}
 */
export function execEquivalence(o) {
  const { file, runtimeA, runtimeB } = o;
  if (!file || !runtimeA || !runtimeB) {
    throw new Error("execEquivalence: file, runtimeA, and runtimeB are required");
  }
  const argv = o.argv ?? [];
  const opts = { cwd: o.cwd, env: o.env ?? process.env, input: o.stdin, encoding: "utf8", maxBuffer: 64 * 1024 * 1024 };
  const A = spawnSync(runtimeA, [file, ...argv], opts);
  const B = spawnSync(runtimeB, [file, ...argv], opts);
  if (A.error) throw new Error(`execEquivalence: runtimeA (${runtimeA}) failed to spawn: ${A.error.message}`);
  if (B.error) throw new Error(`execEquivalence: runtimeB (${runtimeB}) failed to spawn: ${B.error.message}`);

  const aOut = A.stdout ?? "", bOut = B.stdout ?? "";
  const stdoutEqual = aOut === bOut;
  let firstDiffLine = null;
  if (!stdoutEqual) {
    const al = aOut.split("\n"), bl = bOut.split("\n");
    const n = Math.max(al.length, bl.length);
    for (let i = 0; i < n; i++) if (al[i] !== bl[i]) { firstDiffLine = i + 1; break; }
  }
  const exitA = A.status ?? null, exitB = B.status ?? null;
  return { equal: stdoutEqual && exitA === exitB, exitA, exitB, stdoutEqual, firstDiffLine, aStdout: aOut, bStdout: bOut };
}

